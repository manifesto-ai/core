// ============================================================================
// DomainDraft to ManifestoDomain Conversion Tests
// ============================================================================

describe('convertDomainDraftToManifesto', () => {
  describe('basic conversion', () => {
    it('should convert minimal DomainDraft', () => {
      const draft = createMinimalDomainDraft();

      const domain = convertDomainDraftToManifesto(draft);

      expect(domain.id).toBe('test-domain');
      expect(domain.name).toBe('Test Domain');
      expect(domain.description).toBe('A test domain');
      expect(domain.paths.sources).toEqual({});
      expect(domain.paths.derived).toEqual({});
      expect(domain.paths.async).toEqual({});
    });

    it('should use default values when id/name/description are missing', () => {
      const draft = createMinimalDomainDraft({
        id: undefined,
        name: undefined,
        description: undefined,
      });

      const domain = convertDomainDraftToManifesto(draft);

      expect(domain.id).toBe('compiler-generated-domain');
      expect(domain.name).toBe('Generated Domain');
      expect(domain.description).toBe('Domain generated by @manifesto-ai/compiler');
    });

    it('should use custom defaults when provided', () => {
      const draft = createMinimalDomainDraft({
        id: undefined,
        name: undefined,
      });

      const domain = convertDomainDraftToManifesto(draft, {
        defaultId: 'custom-id',
        defaultName: 'Custom Name',
        defaultDescription: 'Custom description',
      });

      expect(domain.id).toBe('custom-id');
      expect(domain.name).toBe('Custom Name');
    });
  });

  describe('schema conversion', () => {
    it('should convert dataSchema to Zod schema', () => {
      const draft = createMinimalDomainDraft({
        dataSchema: {
          count: 'number',
          name: 'string',
          active: 'boolean',
        },
      });

      const domain = convertDomainDraftToManifesto(draft);

      // The dataSchema should be a Zod object
      expect(domain.dataSchema).toBeDefined();
      expect(typeof domain.dataSchema.parse).toBe('function');
    });

    it('should convert stateSchema to Zod schema', () => {
      const draft = createMinimalDomainDraft({
        stateSchema: {
          loading: 'boolean',
          error: 'string',
        },
      });

      const domain = convertDomainDraftToManifesto(draft);

      expect(domain.stateSchema).toBeDefined();
      expect(typeof domain.stateSchema.parse).toBe('function');
    });

    it('should handle optional types', () => {
      const draft = createMinimalDomainDraft({
        dataSchema: {
          optional: 'string?',
        },
      });

      const domain = convertDomainDraftToManifesto(draft);

      // Should parse successfully with undefined
      const result = domain.dataSchema.safeParse({ optional: undefined });
      expect(result.success).toBe(true);
    });

    it('should handle nullable types', () => {
      const draft = createMinimalDomainDraft({
        dataSchema: {
          nullable: 'string | null',
        },
      });

      const domain = convertDomainDraftToManifesto(draft);

      // Should parse successfully with null
      const result = domain.dataSchema.safeParse({ nullable: null });
      expect(result.success).toBe(true);
    });
  });

  describe('sources and derived', () => {
    it('should preserve source definitions', () => {
      const draft = createMinimalDomainDraft({
        sources: {
          'data.input': {
            schema: {} as any, // Will be a Zod schema in practice
            semantic: { type: 'input', description: 'Input field' },
          },
        },
      });

      const domain = convertDomainDraftToManifesto(draft);

      expect(domain.paths.sources['data.input']).toBeDefined();
      expect(domain.paths.sources['data.input'].semantic.type).toBe('input');
    });

    it('should preserve derived definitions', () => {
      const draft = createMinimalDomainDraft({
        derived: {
          'derived.total': {
            deps: ['data.a', 'data.b'],
            expr: ['+', ['get', 'data.a'], ['get', 'data.b']],
            semantic: { type: 'computed', description: 'Sum' },
          },
        },
      });

      const domain = convertDomainDraftToManifesto(draft);

      expect(domain.paths.derived['derived.total']).toBeDefined();
      expect(domain.paths.derived['derived.total'].deps).toEqual(['data.a', 'data.b']);
    });
  });

  describe('actions', () => {
    it('should preserve action definitions', () => {
      const draft = createMinimalDomainDraft({
        actions: {
          submit: {
            deps: ['data.form'],
            effect: { _tag: 'SetValue', path: 'state.submitted' as any, value: true },
            semantic: { type: 'action', description: 'Submit', verb: 'submit' },
          },
        },
      });

      const domain = convertDomainDraftToManifesto(draft);

      expect(domain.actions.submit).toBeDefined();
      expect(domain.actions.submit.deps).toEqual(['data.form']);
    });
  });

  describe('initial state', () => {
    it('should use provided initialState', () => {
      const draft = createMinimalDomainDraft({
        stateSchema: { loading: 'boolean' },
        initialState: { loading: true },
      });

      const domain = convertDomainDraftToManifesto(draft);

      expect(domain.initialState).toEqual({ loading: true });
    });

    it('should build default initialState from schema when not provided', () => {
      const draft = createMinimalDomainDraft({
        stateSchema: { loading: 'boolean', count: 'number' },
        initialState: undefined,
      });

      const domain = convertDomainDraftToManifesto(draft);

      expect(domain.initialState).toHaveProperty('loading');
      expect(domain.initialState).toHaveProperty('count');
    });
  });
});

