describe('full pipeline with Core validation', () => {
  it('should verify complete domain through linker and verifier', () => {
    // Use only SourceFragment for paths (no SchemaFragment to avoid conflict)
    const fragments: Fragment[] = [
      createSourceFragment({
        id: 'source-count',
        provides: ['data.count'],
        path: 'data.count',
        type: 'number',
        schema: { type: 'number' },
        semantic: { type: 'number', description: 'Count value' },
      }),
      createDerivedFragment({
        id: 'derived-doubled',
        provides: ['derived.doubled'],
        requires: ['data.count'],
        path: 'derived.doubled',
        expr: ['*', ['get', 'data.count'], 2],
      }),
      createActionFragment({
        id: 'action-increment',
        provides: ['action:increment'],
        requires: ['data.count'],
        actionId: 'increment',
        effect: { _tag: 'SetValue', path: 'data.count' as any, value: 1 },
      }),
    ];

    // Link fragments
    const linkResult = link(fragments, { buildDomain: true });

    expect(linkResult.domain).toBeDefined();
    expect(linkResult.conflicts).toHaveLength(0);

    // Verify with Core validation
    const verifyResult = verify(linkResult, { useCoreValidation: true });

    // Should pass all validations
    expect(verifyResult.isValid).toBe(true);
    expect(verifyResult.errorCount).toBe(0);
  });

  it('should detect issues in incomplete domain', () => {
    const fragments: Fragment[] = [
      createDerivedFragment({
        id: 'derived-x',
        provides: ['derived.x'],
        requires: ['data.missing'], // Missing dependency
        path: 'derived.x',
      }),
    ];

    const linkResult = link(fragments, { buildDomain: true });
    const verifyResult = verify(linkResult, { useCoreValidation: true });

    expect(verifyResult.isValid).toBe(false);
    expect(verifyResult.issues.some((i) => i.code === 'MISSING_DEPENDENCY')).toBe(true);
  });
});
