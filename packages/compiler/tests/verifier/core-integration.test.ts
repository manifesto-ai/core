describe('verify with useCoreValidation', () => {
  describe('when useCoreValidation=false (default)', () => {
    it('should not call Core validation', () => {
      const fragments: Fragment[] = [
        createSchemaFragment(),
        createDerivedFragment(),
      ];
      const linkResult = link(fragments);

      const result = verify(linkResult, { useCoreValidation: false });

      // Should pass without Core validation
      expect(result.isValid).toBe(true);
    });
  });

  describe('when useCoreValidation=true', () => {
    it('should pass for valid domain', () => {
      // Use only SourceFragment for paths (no SchemaFragment to avoid conflict)
      // SourceFragment provides the path AND creates the source definition
      const fragments: Fragment[] = [
        createSourceFragment({
          id: 'source-count',
          provides: ['data.count'],
          path: 'data.count',
          type: 'number',
          schema: { type: 'number' },
          semantic: { type: 'number', description: 'Count value' },
        }),
        createDerivedFragment({
          id: 'derived-doubled',
          provides: ['derived.doubled'],
          requires: ['data.count'],
        }),
      ];

      const linkResult = link(fragments, { buildDomain: true });

      // Ensure domain was built
      expect(linkResult.domain).toBeDefined();

      // Without Core validation first to establish baseline
      const baseResult = verify(linkResult, { useCoreValidation: false });

      // With Core validation
      const result = verify(linkResult, { useCoreValidation: true });

      // Debug: Log any issues
      if (!result.isValid) {
        console.log('Issues:', result.issues.map(i => `[${i.severity}] ${i.code}: ${i.message}`));
      }

      // Should pass Core validation
      expect(result.isValid).toBe(true);
    });

    it('should skip Core validation when no domain exists', () => {
      const linkResult: LinkResult = {
        fragments: [],
        conflicts: [],
        issues: [],
        version: 'test',
        domain: undefined,
      };

      const result = verify(linkResult, { useCoreValidation: true });

      // Should not fail when domain is missing
      expect(result.isValid).toBe(true);
    });

    it('should handle Core validation errors gracefully', () => {
      // Create a minimal link result with domain
      const linkResult: LinkResult = {
        fragments: [createSchemaFragment()],
        conflicts: [],
        issues: [],
        version: 'test',
        domain: createMinimalDomainDraft(),
      };

      // Core validation should run without throwing
      expect(() => verify(linkResult, { useCoreValidation: true })).not.toThrow();
    });
  });

  describe('determinism (Principle E)', () => {
    it('should produce consistent results with Core validation', () => {
      // Use only SourceFragments for paths (no SchemaFragment to avoid conflict)
      const fragments: Fragment[] = [
        createSourceFragment({
          id: 'source-a',
          provides: ['data.a'],
          path: 'data.a',
          type: 'number',
          schema: { type: 'number' },
          semantic: { type: 'number', description: 'Value A' },
        }),
        createSourceFragment({
          id: 'source-b',
          provides: ['data.b'],
          path: 'data.b',
          type: 'string',
          schema: { type: 'string' },
          semantic: { type: 'string', description: 'Value B' },
        }),
        createDerivedFragment({
          id: 'derived-x',
          provides: ['derived.x'],
          requires: ['data.a'],
        }),
      ];

      const linkResult = link(fragments, { buildDomain: true });

      const result1 = verify(linkResult, { useCoreValidation: true });
      const result2 = verify(linkResult, { useCoreValidation: true });

      expect(result1.isValid).toBe(result2.isValid);
      expect(result1.errorCount).toBe(result2.errorCount);
      expect(result1.warningCount).toBe(result2.warningCount);
      // Compare issue codes instead of IDs (IDs contain random components for uniqueness)
      expect(result1.issues.map((i) => i.code).sort()).toEqual(result2.issues.map((i) => i.code).sort());
    });
  });
});

