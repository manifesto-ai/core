// ============================================================================
// createProvenance
// ============================================================================

describe('createProvenance', () => {
  it('should create provenance with required fields', () => {
    const location = generatedOrigin('test');
    const provenance = createProvenance('artifact_1', location);

    expect(provenance.artifactId).toBe('artifact_1');
    expect(provenance.location).toEqual(location);
    expect(provenance.originHash).toBeUndefined();
    expect(typeof provenance.createdAt).toBe('number');
  });

  it('should include origin hash when provided', () => {
    const location = codeOrigin({ startLine: 1, startCol: 0, endLine: 1, endCol: 10 });
    const provenance = createProvenance('artifact_1', location, 'hash_abc123');

    expect(provenance.originHash).toBe('hash_abc123');
  });

  it('should have current timestamp', () => {
    const before = Date.now();
    const provenance = createProvenance('a', generatedOrigin(''));
    const after = Date.now();

    expect(provenance.createdAt).toBeGreaterThanOrEqual(before);
    expect(provenance.createdAt).toBeLessThanOrEqual(after);
  });

  it('should work with all location types', () => {
    const locations: OriginLocation[] = [
      codeOrigin({ startLine: 1, startCol: 0, endLine: 1, endCol: 10 }),
      textOrigin({ startOffset: 0, endOffset: 100 }),
      generatedOrigin('test'),
      patchOrigin('patch_1'),
      llmOrigin('model-1', 'hash'),
    ];

    for (const location of locations) {
      const provenance = createProvenance('artifact', location);
      expect(provenance.location).toEqual(location);
    }
  });
});

