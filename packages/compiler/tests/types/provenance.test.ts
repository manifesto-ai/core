/**
 * Provenance Types Tests
 *
 * Tests for provenance factory functions in src/types/provenance.ts
 */

import { describe, it, expect } from 'vitest';
import {
  codeOrigin,
  textOrigin,
  generatedOrigin,
  patchOrigin,
  llmOrigin,
  createProvenance,
  createEvidence,
  type CodeSpan,
  type TextSpan,
  type OriginLocation,
  type Provenance,
  type Evidence,
  type EvidenceKind,
} from '../../src/types/provenance.js';

// ============================================================================
// codeOrigin
// ============================================================================

describe('codeOrigin', () => {
  it('should create code origin with span', () => {
    const span: CodeSpan = {
      file: 'src/main.ts',
      startLine: 10,
      startCol: 0,
      endLine: 15,
      endCol: 20,
    };

    const origin = codeOrigin(span);

    expect(origin.kind).toBe('code');
    expect((origin as any).span).toEqual(span);
  });

  it('should work without optional file', () => {
    const span: CodeSpan = {
      startLine: 1,
      startCol: 0,
      endLine: 1,
      endCol: 10,
    };

    const origin = codeOrigin(span);

    expect(origin.kind).toBe('code');
    expect((origin as any).span.file).toBeUndefined();
  });

  it('should preserve all span properties', () => {
    const span: CodeSpan = {
      file: 'test.js',
      startLine: 100,
      startCol: 50,
      endLine: 200,
      endCol: 100,
    };

    const origin = codeOrigin(span) as { kind: 'code'; span: CodeSpan };

    expect(origin.span.file).toBe('test.js');
    expect(origin.span.startLine).toBe(100);
    expect(origin.span.startCol).toBe(50);
    expect(origin.span.endLine).toBe(200);
    expect(origin.span.endCol).toBe(100);
  });
});

