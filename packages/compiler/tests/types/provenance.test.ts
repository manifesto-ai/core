// createEvidence
// ============================================================================

describe('createEvidence', () => {
  it('should create evidence with required fields', () => {
    const evidence = createEvidence('quote', 'source.ts:10');

    expect(evidence.kind).toBe('quote');
    expect(evidence.ref).toBe('source.ts:10');
    expect(evidence.excerpt).toBeUndefined();
    expect(evidence.confidence).toBeUndefined();
  });

  it('should create evidence with excerpt', () => {
    const evidence = createEvidence('quote', 'doc.md:5', 'User authentication flow');

    expect(evidence.excerpt).toBe('User authentication flow');
  });

  it('should create evidence with confidence', () => {
    const evidence = createEvidence('llm_reasoning', 'chain-of-thought', undefined, 0.85);

    expect(evidence.kind).toBe('llm_reasoning');
    expect(evidence.confidence).toBe(0.85);
  });

  it('should create evidence with all optional fields', () => {
    const evidence = createEvidence(
      'ast_node',
      'FunctionDeclaration',
      'function doSomething()',
      0.95
    );

    expect(evidence.kind).toBe('ast_node');
    expect(evidence.ref).toBe('FunctionDeclaration');
    expect(evidence.excerpt).toBe('function doSomething()');
    expect(evidence.confidence).toBe(0.95);
  });

  it('should handle all evidence kinds', () => {
    const kinds: EvidenceKind[] = [
      'quote',
      'ast_node',
      'rule',
      'link',
      'fragment_ref',
      'llm_reasoning',
    ];

    for (const kind of kinds) {
      const evidence = createEvidence(kind, 'ref');
      expect(evidence.kind).toBe(kind);
    }
  });
});

// ============================================================================
// Edge Cases
// ============================================================================

describe('Edge Cases', () => {
  it('should handle zero line/column numbers', () => {
    const span: CodeSpan = {
      startLine: 0,
      startCol: 0,
      endLine: 0,
      endCol: 0,
    };

    const origin = codeOrigin(span);
    expect((origin as any).span.startLine).toBe(0);
  });

  it('should handle large offset values', () => {
    const span: TextSpan = {
      startOffset: Number.MAX_SAFE_INTEGER - 1,
      endOffset: Number.MAX_SAFE_INTEGER,
    };

    const origin = textOrigin(span);
    expect((origin as any).span.startOffset).toBe(Number.MAX_SAFE_INTEGER - 1);
  });

  it('should handle unicode in references', () => {
    const evidence = createEvidence('quote', '소스코드.ts:10', '한글 주석');

    expect(evidence.ref).toBe('소스코드.ts:10');
    expect(evidence.excerpt).toBe('한글 주석');
  });

  it('should handle empty strings', () => {
    const origin = generatedOrigin('');
    expect((origin as any).note).toBe('');

    const evidence = createEvidence('rule', '', '');
    expect(evidence.ref).toBe('');
    expect(evidence.excerpt).toBe('');
  });

  it('should handle special characters in artifact ID', () => {
    const provenance = createProvenance(
      'artifact-with_special.chars:123',
      generatedOrigin('test')
    );

    expect(provenance.artifactId).toBe('artifact-with_special.chars:123');
  });

  it('should handle confidence edge values', () => {
    const zeroConf = createEvidence('llm_reasoning', 'ref', undefined, 0);
    expect(zeroConf.confidence).toBe(0);

    const oneConf = createEvidence('llm_reasoning', 'ref', undefined, 1);
    expect(oneConf.confidence).toBe(1);
  });
});
